num1 = int(input("Enter Your num1 : ")) # to get input from user.
num2 = int(input("Enter Your num2 : ")) # to get input from user.
print("Num1 : ",num1) # to print user input.
print("Num2 : ",num2) # to print user input.

# swap using third variable.
temp = num1 
num1 = num2
num2 = temp

print("New Num1 : ",num1) # to print the swapped value.
print("New Num2 : ",num2) # to print the swapped value.